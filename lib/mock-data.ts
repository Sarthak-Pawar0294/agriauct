// Types for AgriAuct

export type UserRole = "farmer" | "vendor"

export interface User {
  id: string
  fullName: string
  email: string
  phone: string
  state: string
  district: string
  role: UserRole
  createdAt: Date
}

export type Category = "vegetables" | "fruits" | "grains" | "other"
export type QualityGrade = "A" | "B" | "C"
export type ListingStatus = "active" | "closed"

export interface Listing {
  id: string
  farmerId: string
  farmerName: string
  cropName: string
  category: Category
  quantity: number
  qualityGrade: QualityGrade
  state: string
  district: string
  startingPrice: number
  currentHighestBid: number
  auctionDuration: number
  startTime: Date
  endTime: Date
  status: ListingStatus
  imageUrl: string
  bidCount: number
  winnerId?: string
  winnerName?: string
}

export interface Bid {
  id: string
  listingId: string
  vendorId: string
  vendorName: string
  amount: number
  createdAt: Date
}

export interface Notification {
  id: string
  userId: string
  message: string
  isRead: boolean
  createdAt: Date
  type: "outbid" | "won" | "auction_ended" | "new_bid"
  listingId?: string
}

